package com.java.demo;

public class nineninetable {
    public static void main(String[] args){
        for (int i = 1;i < 10;i++){
            for (int k = 1;k <=i ; k++){
                System.out.print(i + "x" + k + "=" + i*k + " ");
            }
            System.out.println();
        }

    }
}
